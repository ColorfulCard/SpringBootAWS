package com.example.demo.model;

public class HeartPress {
	
	private int pno;
	private String hid;
	
	public HeartPress(int pno, String hid) {
		super();
		this.pno = pno;
		this.hid = hid;
	}

	public int getPno() {
		return pno;
	}

	public void setPno(int pno) {
		this.pno = pno;
	}

	public String getHid() {
		return hid;
	}

	public void setHid(String hid) {
		this.hid = hid;
	}
	
	

}
